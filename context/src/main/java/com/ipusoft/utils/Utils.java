public class Utils {

    public static int parseInteger(Integer value) {
        int result = 0;
        if (value != null) {
            result = value;
        }
        return result;
    }

    public static long parseLong(Long value) {
        long result = 0;
        if (value != null) {
            result = value;
        }
        return result;
    }
}
